import sys
from models.packets import packets, packet
from util import model_loader
from util import redo


def _warn(msg):
    """Emit a soft warning to stderr -- does not abort the build."""
    sys.stderr.write("Warning: " + msg + "\n")


def _resolve_record_model(table_package_name):
    """
    Map a parameter-table package name to its auto-generated record model
    (<Name> -> <Name>_record, e.g. Example_Parameter_Table ->
    Example_Parameter_Table_record). Every packet that gets typed comes
    from a .parameter_table.yaml, which always emits the "_record" suffix;
    hand-authored record tables (no .parameter_table.yaml) cannot reach
    type resolution and are left typeless instead. Returns
    (model_name, model_path) or (None, None).
    """
    model_name = table_package_name + "_record"
    path = model_loader.get_model_file_path(model_name, model_types=["record"])
    if path:
        return model_name, path
    return None, None


class parameters_packets(packets):
    """
    This model is a specialized packet suite for the Parameters packet. This
    class inherits from the normal packets model but adds some extra functionality
    needed to the parameters packet. In particular, the parameters packet's
    type is derived from data within the assembly that the packet is a part of. The
    type itself gets filled in in the "load_assembly" method.
    """
    def submodel_name(self):
        """This is just a "decorated" packet object, so make sure everyone treats it like that."""
        return "packets"

    def _replace_packet_type(self, key, pkt, model_name, model_path):
        """
        Helper -- swap a typeless packet entry in self.entities for one
        whose payload type is the record model at model_path. Also registers
        the type model with the suite's complex_types so downstream
        templates/generators see the dependency.
        """
        # Make sure the model file has been autogenerated. Note that this
        # causes a circular dependency loop if shallow_load is not set to
        # True in the generator for the packet type.
        redo.redo_ifchange(model_path)

        self.entities[key] = packet(
            name=pkt.name,
            type=model_name + ".T",
            description=pkt.description,
            id=pkt.id,
            suite=pkt.suite,
        )

        # Register the type with this suite and the component's complex types
        # so other tooling sees it.
        if self.entities[key].type_model is not None:
            self.type_models.append(self.entities[key].type_model)
            self.component.complex_types[
                self.entities[key].type_model.name
            ] = self.entities[key].type_model

    def _resolve_from_parameters_init(self, component):
        """
        Resolve a Parameters component's table type from its
        parameter_Table_Entries init parameter. The init value looks like
        "<Pkg>.Parameter_Table_Entries"; the package name <Pkg> is the
        record model. Returns (model_name, model_path) or (None, None).
        """
        # Contract: only the Parameters component owns its table type via the
        # parameter_Table_Entries init parameter.
        assert component.name == "Parameters", \
            "expected a Parameters component, got '" + str(component.name) + "'"
        try:
            param_table = component.init.get_parameter_value(
                "parameter_Table_Entries"
            )
        except Exception:
            return None, None
        if not param_table:
            return None, None
        return _resolve_record_model(param_table.split(".")[0])

    def _resolve_via_parameter_manager(self):
        """
        Parameter_Store paired via Parameter_Manager. Walk to the manager
        that sends to this component, then to the Parameters component on
        the manager's working side, and resolve its parameter_Table_Entries.
        Returns (model_name, model_path) or (None, None).
        """
        # Contract: this discovery strategy applies only to a Parameter_Store.
        assert self.component.name == "Parameter_Store", \
            "expected a Parameter_Store component, got '" + str(self.component.name) + "'"
        param_manager = None
        for conn in self.assembly.connections:
            if (
                conn.to_component is self.component
                and conn.to_connector.name == "Parameters_Memory_Region_T_Recv_Async"
            ):
                # Only Parameter_Manager exposes this exact connection name
                # from a routing component (the Ccsds_Parameter_Table_Router
                # uses an arrayed Parameters_Memory_Region_T_Send). We still
                # need to confirm the upstream is a Parameter_Manager.
                if conn.from_component.name == "Parameter_Manager":
                    param_manager = conn.from_component
                    break

        if param_manager is None:
            return None, None

        for conn in self.assembly.connections:
            if (
                conn.from_component is param_manager
                and conn.to_component.name == "Parameters"
            ):
                return self._resolve_from_parameters_init(conn.to_component)
        return None, None

    def _resolve_via_router_table(self):
        """
        Parameter_Store listed as a destination in a
        Ccsds_Parameter_Table_Router router table colocated with the
        assembly. When the entry's table_id is a named constant,
        original_table_id IS the parameter table package name (e.g.
        Example_Parameter_Table) and is used directly.

        Numeric table_ids cannot be resolved this way and fall through to
        a typeless packet. In practice the only numeric-table_id stores
        are large tables that dump via Memory_Dump_Send, which are handled
        as typeless by the _is_memory_dump_path() skip in set_assembly
        before this branch ever runs.

        Returns (model_name, model_path) or (None, None).
        """
        # Contract: this discovery strategy applies only to a Parameter_Store.
        assert self.component.name == "Parameter_Store", \
            "expected a Parameter_Store component, got '" + str(self.component.name) + "'"
        router = model_loader.try_load_model_by_name(
            self.assembly.model_name,
            model_types=["parameter_table_router_table"],
        )
        if router is None:
            return None, None
        for entry in router.table.values():
            names = [d.component_name for d in entry.destinations]
            if self.component.instance_name not in names:
                continue
            if isinstance(entry.original_table_id, str):
                return _resolve_record_model(entry.original_table_id)
            # Numeric table_id: cannot infer the table type from the router.
            return None, None
        return None, None

    def _resolve_packet_type(self, pkt):
        """
        Dispatch to the right discovery branch for the component owning this
        packet. Returns (model_name, model_path) or (None, None).

        Note: Parameter_Table_Forwarder packets are not dispatched here.
        The forwarder's only dump path is Memory_Dump_Send (it has no
        Packet_T_Send connector), so its packet is always typeless and is
        skipped by the _is_memory_dump_path() check in set_assembly before
        reaching this dispatch.
        """
        comp_type = self.component.name
        if comp_type == "Parameters" and pkt.name == "Active_Parameters":
            return self._resolve_from_parameters_init(self.component)
        if comp_type == "Parameter_Store":
            # Try the Parameter_Manager topology first (back-compat for
            # assemblies that use it), then fall back to the router table.
            mn, mp = self._resolve_via_parameter_manager()
            if mn:
                return mn, mp
            return self._resolve_via_router_table()
        return None, None

    def _is_memory_dump_path(self):
        """
        True if this component's dump path is Memory_Dump_Send (the large-
        table pathway through a downstream Memory_Packetizer), rather than
        Packet_T_Send (single-packet path). The Memory_Dump path implies
        the table physically downlinks as several chunks under one APID --
        a single "flat record" packet type is fictional in that case. We
        leave such packets typeless so they render as opaque byte arrays
        in cosmos (mirroring Memory_Dumper's Memory_Dump_Packet shape).
        """
        for conn in self.assembly.connections:
            if (
                conn.from_component is self.component
                and conn.from_connector is not None
                and conn.from_connector.name == "Memory_Dump_Send"
            ):
                return True
        return False

    def set_assembly(self, assembly):
        # Set assembly:
        self.assembly = assembly

        # Replace each Active_Parameters / Stored_Parameters / etc. packet
        # in self.entities with a typed packet, using a per-component-type
        # discovery rule. If a packet cannot be resolved, leave it as-is
        # and emit a soft warning -- the packet stays typeless rather than
        # aborting the build.
        for key, pkt in list(self.entities.items()):
            # Only consider packets whose name suggests they carry a
            # parameter table snapshot. This keeps unrelated packets on
            # the same component untouched.
            if not pkt.name.endswith("Parameters"):
                continue

            # Skip type binding for components that dump via Memory_Dump_
            # Send. Those tables exceed Packet_Buffer_Size and downlink as
            # multiple chunks reassembled on ground; the packet stays
            # typeless so cosmos renders it as an opaque BLOCK, matching
            # Memory_Dumper.Memory_Dump_Packet. Field decoding is a
            # ground-side concern for these.
            if self._is_memory_dump_path():
                continue

            model_name, model_path = self._resolve_packet_type(pkt)
            if model_path:
                self._replace_packet_type(key, pkt, model_name, model_path)
            else:
                _warn(
                    "Could not resolve parameter-table type for packet '"
                    + pkt.name
                    + "' on component instance '"
                    + str(self.component.instance_name)
                    + "' (type '"
                    + str(self.component.name)
                    + "'). The packet will remain typeless. Check that the "
                    "component is wired through a Parameter_Manager, or is "
                    "listed in a parameter_table_router table with a named "
                    "table_id."
                )

        # Call the base class version:
        super(parameters_packets, self).set_assembly(assembly)
