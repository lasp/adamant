"""
Generates one <Sequence_Name>_Run_Arg.record.yaml per declared sequence in any
command_sequences.yaml in the build path. Each wrapped record carries the
sequence's native arg type (if any) plus a trailing Response_Behavior field, so
the autocoded per-sequence command (Phase 2) can be invoked with the typed form:

    Sequence_B ((Component_A_Arg => ..., Component_B_Arg => ...),
                Send_After_Sequence_Completion)

The framework's generator base requires one output file per (generator, input)
pair, so to produce N record yamls from one command_sequences.yaml we register
N generator classes here, each with a fixed seq_index. We scan the project
filesystem at module-load time to determine the smallest N that covers every
command_sequences.yaml in the tree -- no fixed pre-allocation.
"""

import os
import glob
import yaml as _yaml

from base_classes.generator_base import generator_base
from generators.basic import basic_generator, add_class_to_module
# Imported only to key this generator to *.command_sequences.yaml inputs (via
# model_class.__name__). We never INSTANTIATE the model here -- doing so would
# be circular, since the model now requires the very records we generate. See
# _raw_sequences_in below.
from models import command_sequences as _cs_module


# ---------------------------------------------------------------------------
# Module-load filesystem scan
# ---------------------------------------------------------------------------
def _find_max_sequence_count():
    """
    Walk the build roots at module-load time, parse every
    *.command_sequences.yaml found, and return the maximum sequence count
    across all of them. This is the number of wrapped-record generator
    classes we need to register so that every sequence gets its own output
    file.
    """
    roots = []
    for var in ("BUILD_ROOTS", "EXTRA_BUILD_ROOTS", "COMPUTED_BUILD_ROOTS"):
        raw = os.environ.get(var)
        if raw:
            roots.extend(r for r in raw.split(":") if r)
    if not roots:
        roots = [os.environ.get("ADAMANT_DIR", os.getcwd())]

    max_n = 0
    seen = set()
    for root in roots:
        pattern = os.path.join(root, "**", "*.command_sequences.yaml")
        for path in glob.iglob(pattern, recursive=True):
            real = os.path.realpath(path)
            if real in seen:
                continue
            seen.add(real)
            try:
                with open(real) as f:
                    data = _yaml.safe_load(f)
            except Exception:
                # Tolerate parse errors at module-load -- the real load later
                # will surface them with proper context.
                continue
            if isinstance(data, dict) and "sequences" in data and data["sequences"]:
                max_n = max(max_n, len(data["sequences"]))
    return max_n


def _raw_sequences_in(input_filename):
    """
    Return the sequences declared in a command_sequences.yaml as plain dicts
    read straight from the raw YAML.

    IMPORTANT: this deliberately does NOT construct the command_sequences
    model. That model's command_sequence.from_sequence_data now requires each
    sequence's <Name>_Run_Arg.record.yaml to already exist (it sets the
    command's arg type to the wrapped record and redo_ifchange's it). Since
    THIS generator is what produces those records, going through the model
    here would be a circular dependency -- the model can't load until the
    records exist, and the records can't be generated until we know the
    sequence names. So we read only what we need (name + native arg_type)
    from the raw file.
    """
    with open(input_filename) as f:
        data = _yaml.safe_load(f)
    if not isinstance(data, dict) or not data.get("sequences"):
        return []
    return [
        {"name": s["name"], "arg_type": s.get("arg_type", None)}
        for s in data["sequences"]
    ]


def _render_wrapped_record_yaml(seq):
    """Produce the body of one <Sequence>_Run_Arg.record.yaml file.

    `seq` is a raw dict: {"name": ..., "arg_type": <native arg type or None>}.
    The User_Args field carries the sequence's NATIVE arg type (e.g.
    Sequence_B_Arg.T), not the wrapped record -- the model overwrites
    seq.arg_type with the wrapped name, so we must read the native type from
    the raw YAML here.
    """
    name = seq["name"]
    native_arg_type = seq["arg_type"]
    lines = [
        "---",
        f"description: Wrapped argument record for sequence {name}. "
        "Auto-generated by the simple command sequencer wrapped_run_args generator -- do not edit by hand.",
        "fields:",
    ]
    if native_arg_type:
        lines.append("  - name: User_Args")
        lines.append("    description: Sequence-specific user argument.")
        lines.append(f"    type: {native_arg_type}")
    lines.append("  - name: Response_Behavior")
    lines.append(
        "    description: When the sequencer should reply to the Run_Sequence command - "
        "immediately on start or after the sequence completes."
    )
    lines.append("    type: Sequence_Enums.Sequence_Response_Behavior.E")
    lines.append("    format: E8")
    return "\n".join(lines) + "\n"


# ---------------------------------------------------------------------------
# Generator base class -- one concrete subclass per seq_index
# ---------------------------------------------------------------------------
class _wrapped_run_arg_record_yaml_base(basic_generator, generator_base):
    # Each concrete subclass sets a unique SEQ_INDEX (0, 1, 2, ...).
    SEQ_INDEX = None

    def __init__(self):
        this_file_dir = os.path.dirname(os.path.realpath(__file__))
        template_dir = os.path.join(this_file_dir, ".." + os.sep + "templates")
        basic_generator.__init__(
            self,
            model_class=_cs_module.command_sequences,
            # template_filename is required by basic_generator but we override
            # generate() to bypass the Jinja render path -- the value just has
            # to be syntactically a valid name with the right extension so
            # output-dir defaults work out (".yaml" -> build/yaml).
            template_filename="name_run_arg.record.yaml",
            template_dir=template_dir,
        )

    def output_filename(self, input_filename):
        # SEQ_INDEX is None on the abstract base; only concrete subclasses
        # created via type() below carry a real index.
        if self.SEQ_INDEX is None:
            return ""
        seqs = _raw_sequences_in(input_filename)
        if self.SEQ_INDEX >= len(seqs):
            # No sequence at this index for this input -- suppress registration.
            return ""
        seq = seqs[self.SEQ_INDEX]
        dirname, *_ = self._split_input_filename(input_filename)
        return os.path.join(
            dirname,
            "build",
            "yaml",
            f"{seq['name'].lower()}_run_arg.record.yaml",
        )

    def generate(self, input_filename):
        if self.SEQ_INDEX is None:
            return
        seqs = _raw_sequences_in(input_filename)
        if self.SEQ_INDEX >= len(seqs):
            return
        seq = seqs[self.SEQ_INDEX]
        print(_render_wrapped_record_yaml(seq), end="")


# ---------------------------------------------------------------------------
# Register exactly enough concrete subclasses to cover every sequence in
# every command_sequences.yaml found at module-load time.
# ---------------------------------------------------------------------------
_MAX_SEQUENCES = _find_max_sequence_count()
for _i in range(_MAX_SEQUENCES):
    _cls = type(
        f"wrapped_run_arg_record_yaml_{_i}",
        (_wrapped_run_arg_record_yaml_base,),
        {"SEQ_INDEX": _i, "__module__": __name__},
    )
    add_class_to_module(_cls, globals())
